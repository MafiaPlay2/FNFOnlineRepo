function onBeatHit()

	if curBeat == 136 then
		cameraFlash('game', 'FFFFFF', 0.4, false)
	end
	
	if curBeat == 584 then
		cameraFlash('game', 'FFFFFF', 0.5, false)
	end
	
	if curBeat == 680 then
		cameraFlash('game', 'FFFFFF', 0.4, false)
	end
	
	if curBeat == 728 then
		cameraFlash('game', 'FFFFFF', 3, false)
	end
end
	