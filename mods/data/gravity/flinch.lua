function onBeatHit()


	if curBeat >= 340 and curBeat < 341 then
	characterPlayAnim('dad','Flinch A', true, false, 0)
	end
	
	if curBeat >= 343 and curBeat < 344 then
	characterPlayAnim('dad', 'Flinch B', true, false, 0)
	end
	
	
	if curBeat >= 366 and curBeat < 367 then
	characterPlayAnim('dad','Snap A', true, false, 0)
	end
	
	if curBeat >= 367 and curBeat < 368 then
	characterPlayAnim('dad','Snap B', true, false, 0)
	end
end
	